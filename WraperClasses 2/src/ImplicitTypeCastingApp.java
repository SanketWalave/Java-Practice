/*Implicit type casting : implicit type casting means those
conversions performed by the compiler internally called implicit type casting.*/
public class ImplicitTypeCastingApp {
    public static void main(String[] args) {
        long a;
        int b=100;
        a=b;
        System.out.println(a);
        /*When we have larger data type at left hand side and
         smaller data type at right hand side then
         the compiler is able to perform conversion automatically
         called implicit conversion.*/
    }
}
